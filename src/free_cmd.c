#include "sh.h"

void	free_cmd(t_env *e)
{
	int	i;

	i = 0;
	while (e->cmd[i])
	{
		free(e->cmd[i]);
		e->cmd[i] = NULL;
		i++;
	}
	free(e->cmd);
	e->cmd = NULL;
}

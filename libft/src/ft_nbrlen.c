#include "libft.h"

unsigned int	ft_nbrlen(long long n)
{
	unsigned int i;

	i = 1;
	if (n < 0)
		i++;
	while (n >= 10 || n <= -10)
	{
		i++;
		n /= 10;
	}
	return (i);
}

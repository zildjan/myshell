#include "libft.h"

size_t	ft_putstr(char const *s)
{
	size_t i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
			i++;
		if (i != 0)
			write(1, s, i);
	}
	else
	{
		i = 6;
		write(1, "(null)", i);
	}
	return (i);
}
